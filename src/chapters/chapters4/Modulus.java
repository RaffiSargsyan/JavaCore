package chapters.chapters4;

public class Modulus {
    public static void main(String[] args) {
        int x = 42;
        double y = 42.25;
        System.out.println("x mod 10 = " + x % 10);
        System.out.println("y mod 10 = " + y % 10);
        int a;
        a = 4;
        a += 5;// or a = a + 5
        System.out.println(a);
        a = a % 2; // or a %=2
        int k, l, z;
        k = l = z = 100;
        System.out.println("l = " + l + " z = " + z);
    }
}

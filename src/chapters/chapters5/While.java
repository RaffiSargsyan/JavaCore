package chapters.chapters5;

public class While {
    public static void main(String[] args) {
        int n = 10;
        while (n > 0) {
            System.out.println("такт " + n);
            n--;
        }
        System.out.println();
        int x = 20;
        while (x>0){
            System.out.println("такт "+ x);
            x --;
        }
    }
}

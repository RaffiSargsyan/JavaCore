package chapters.chapters2;

public class ForTest {
    public static void main(String[] args) {
        int x;
        for (x = 0; x <= 10; x++) {
            System.out.println("Value x: " + x);
        }
        System.out.println("\n");

        for (int i = 10; i >= 0; i--) {
            System.out.println("Value i: " + i);
        }
    }
}




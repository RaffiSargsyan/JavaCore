package classwork.calculator;

public class Calculator {


    double plus(double num, double num1) {
        return num + num1;
    }

    double minus(double num, double num1) {
        return num - num1;
    }

    double multiply(double num, double num1) {
        return num * num1;
    }

    double divide(double num, double num1) {
        return num / num1;
    }
}
